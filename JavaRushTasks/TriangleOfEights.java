package JavaRushTasks;

public class TriangleOfEights {
    public static void main(String[] args) {
        int a = 1;
        while (a <= 10) {

            for (int i = 0; i < a; i++) {
                System.out.print(8);
            }
            System.out.println();
            a++;

        }
    }
}
